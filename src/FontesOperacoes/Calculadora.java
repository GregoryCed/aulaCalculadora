package FontesOperacoes;
public class Calculadora {	
	
	public int somar(int num1, int num2) {		 	
		return(num1 + num2);
	}
	
	public int subtrair(int num1, int num2) {
		return(num2 - num1);
	}
	
	public int multiplicacao(int num1, int num2) {
		return(num1 * num2);
	}
	
	public double divisao(int num1, int num2) {
		return(num2 / num1);
	}
	
	
}
